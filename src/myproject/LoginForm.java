    private void jButton1_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_loginActionPerformed
        
        jLabel4_indicator_1.setVisible(false);
        jLabel5_indicator_2.setVisible(false);
        
        if(jTextField1_username.getText().equals("")){ 
                        jLabel4_indicator_1.setVisible(true);
        }
        if (String.valueOf(jPasswordField1.getPassword()).equals("")) {
                jLabel5_indicator_2.setVisible(true);
            }
        else {
            Connection con = DbConnection.getConnection();
            PreparedStatement ps;
            
            try {
            ps = con.prepareStatement("SELECT * FROM user WHERE username = ? AND password = ?");
            ps.setString(1, jTextField1_username.getText());
            ps.setString(2, String.valueOf(jPasswordField1.getPassword()));
            
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()) {
                MainForm mf = new MainForm();
                mf.setVisible(true);
                mf.pack();
                mf.setLocationRelativeTo(null);
                this.dispose();
                MainForm.jLabel1_welcomeUsername.setText("Welcome "+  jTextField1_username.getText());
            } else { 
                JOptionPane.showMessageDialog(null,"Wrong Username or Password","Login Failed",2);
            }
            
            } catch (SQLException ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1_loginActionPerformed

