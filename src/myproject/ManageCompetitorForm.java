    public boolean textPresentVerifier() {
        if (firstNameTextField.getText().equals("") || secondNameTextField.getText().equals("") || addressTextField.getText().equals("") || clubNameTextField.getText().equals("") || phoneNumberTextField.getText().equals("") || birthdateDateChooser.getDate() == null || idTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "There Cannot Be Empty Fields!");
            return false;
            //Date needs to be higher than current date
        } else if (birthdateDateChooser.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(null, "Competitor Cannot Be Born In The Future!");
            return false;
        } else {

            return true;
        }
    }

