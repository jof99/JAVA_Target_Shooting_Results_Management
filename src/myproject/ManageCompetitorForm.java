    private void competitorsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_competitorsTableMouseClicked
        // TODO add your handling code here:
        model = (DefaultTableModel) competitorsTable.getModel();
        rowIndex = competitorsTable.getSelectedRow();
        femaleRadioButton.setSelected(false);
        maleRadioButton.setSelected(false);

        if (model.getValueAt(rowIndex, 1).toString().equals("Male")) {
            maleRadioButton.setSelected(true);
        } else {
            femaleRadioButton.setSelected(true);
        }

        idTextField.setText(model.getValueAt(rowIndex, 0).toString());
        firstNameTextField.setText(model.getValueAt(rowIndex, 2).toString());
        secondNameTextField.setText(model.getValueAt(rowIndex, 3).toString());
        addressTextField.setText(model.getValueAt(rowIndex, 4).toString());
        clubNameTextField.setText(model.getValueAt(rowIndex, 5).toString());
        phoneNumberTextField.setText(model.getValueAt(rowIndex, 7).toString());
        gameIdTextField.setText(model.getValueAt(rowIndex, 8).toString());
        initialScoreAverageTextField.setText(model.getValueAt(rowIndex, 10).toString());

        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 6).toString());
            birthdateDateChooser.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(ManageCompetitorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_competitorsTableMouseClicked

