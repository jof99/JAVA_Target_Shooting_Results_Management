    private void editCompetitorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCompetitorButtonActionPerformed
        String gender = "";
        if (femaleRadioButton.isSelected()) {
            gender = "Female";
        } //else if (jRadioButton1_male.isSelected()) {
        // gender = "Male";
        //}
       
        double initialScoreAverage =  Double.parseDouble(initialScoreAverageTextField.getText());
        String firstName = firstNameTextField.getText();
        String secondName = secondNameTextField.getText();
        String address = addressTextField.getText();
        String clubName = clubNameTextField.getText();
        String phoneNumber = phoneNumberTextField.getText();
        

        int id = Integer.valueOf(idTextField.getText());
        int gameId = Integer.valueOf(gameIdTextField.getText());

        if (textPresentVerifier()) {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String birthdate = dateFormat.format(birthdateDateChooser.getDate());
            GameManager.getSingleton().InsertDeleteUpdateCompetitor('u', id, firstName, secondName, gender, address, clubName, birthdate, phoneNumber, gameId, initialScoreAverage);
            GameManager.getSingleton().populateCompetitorJtable(ManageCompetitorForm.competitorsTable, "");
        }
    }//GEN-LAST:event_editCompetitorButtonActionPerformed

    private void removeCompetitorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeCompetitorButtonActionPerformed
        // Deletes score associated with student by adding a constraint to the score table which is the foriegn key and delete on cascade

//        ALTER TABLE score
//        add CONSTRAINT foriegnKeyScore
//        FOREIGN KEY(`competitor_id`)
//        REFERENCES competitor (Id)
//        on DELETE CASCADE
        if (idTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No Competitor Selected");
        } else {
            
            int id = Integer.valueOf(idTextField.getText());
            GameManager.getSingleton().InsertDeleteUpdateCompetitor('d', id, null, null, null, null, null, null, null, 0,0);
            GameManager.getSingleton().populateCompetitorJtable(competitorsTable, "");
            competitorsTable.setModel(new DefaultTableModel(null, new Object[]{"ID", "Gender", "First Name", "Second Name", "Address", "Clubname", "Birthdate", "Phone Number", "Game ID"}));
            GameManager.getSingleton().populateCompetitorJtable(competitorsTable, searchBarTextField.getText());
            ManageCompetitorForm.numberOfCompetitorsTitle.setText("Competitor Count: " + Integer.toString(Functions.countData("competitor")));
            idTextField.setText("");
            initialScoreAverageTextField.setText("");
            firstNameTextField.setText("");
            secondNameTextField.setText("");
            clubNameTextField.setText("");
            addressTextField.setText("");
            phoneNumberTextField.setText("");
            maleRadioButton.setSelected(false);
            femaleRadioButton.setSelected(false);
            birthdateDateChooser.setDate(null);
            gameIdTextField.setText("");
        }

    }//GEN-LAST:event_removeCompetitorButtonActionPerformed
