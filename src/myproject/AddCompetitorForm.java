    public boolean textPresentVerifier() { 
        if(firstNameTextField.getText().equals("") || secondNameTextField.getText().equals("") || addressTextField.getText().equals("") || clubNameTextField.getText().equals("") || phoneNumberTextField.getText().equals("") || inititalScoreAverageTextField.getText().equals("")|| birthdateDateChooser.getDate() == null) {
                        JOptionPane.showMessageDialog(null, "There Cannot Be Empty Fields!");
            return false;
            //Date needs to be higher than current date
        } else if(birthdateDateChooser.getDate().compareTo(new Date()) > 0){
            JOptionPane.showMessageDialog(null, "Competitor Cannot Be Born In The Future!");
            return false;
        } else {
           
            return true;
        }
    }
    
    
    private void addNewCompetitorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewCompetitorButtonActionPerformed
        String gender = "Male";
        if(femaleGenderRadioButton.isSelected()) { 
            gender = "Female";
        }
        String firstName = firstNameTextField.getText();
        String secondName = secondNameTextField.getText();
        String address = addressTextField.getText();
        String clubName = clubNameTextField.getText();
        String phoneNumber = phoneNumberTextField.getText();
        
        if(inititalScoreAverageTextField.getText().length() > 3) { 
             JOptionPane.showMessageDialog(null, "Can only be triple digit score");
             return;
         }
        double initialScoreAverage =  Double.parseDouble(inititalScoreAverageTextField.getText());

        String selectGame = (String) gameComboBox.getSelectedItem();
        int selectedGameId = GameManager.getSingleton().getIdForGameName(selectGame);
        System.out.println(selectedGameId);
        
        if(textPresentVerifier()) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String birthdate = dateFormat.format(birthdateDateChooser.getDate());
        
        GameManager.getSingleton().InsertDeleteUpdateCompetitor('i', null, firstName, secondName, gender, address, clubName, birthdate, phoneNumber,selectedGameId, initialScoreAverage);
       
        
        }
        try {
        ManageCompetitorForm.competitorsTable.setModel(new DefaultTableModel(null,new Object[]{"ID" , "Gender" , "First Name" , "Second Name" , "Address", "Clubname", "Birthdate", "Phone Number"}));
        GameManager.getSingleton().populateCompetitorJtable(ManageCompetitorForm.competitorsTable, "");
        } catch(Exception ex){
            System.out.println(ex.getMessage());    
        }
        
    }//GEN-LAST:event_addNewCompetitorButtonActionPerformed

